def build_reviews_msg() -> str:
    """Build a reviews message with 4 random reviews each call."""
    sample = random.sample(REVIEWS_DATA, min(4, len(REVIEWS_DATA)))
    lines = ["⭐ <b>מה הלקוחות שלנו אומרים?</b>\n"]
    for r in sample:
        lines.append(
            f"━━━━━━━━━━━━━━━━━━━━━\n"
            f"{r['platform']}\n"
            f"{r['stars']}\n"
            f"<i>\"{r['text']}\"</i>\n"
            f"👤 {r['handle']}"
        )
    lines.append("\n━━━━━━━━━━━━━━━━━━━━━")
    lines.append(
        f"💬 <b>רוצה תוצאות כמותם?</b>\n"
        f"👉 <a href='https://t.me/{OWNER_USERNAME}'>@{OWNER_USERNAME}</a> — נסגור עסקה 🤝"
    )
    return "\n".join(lines)


